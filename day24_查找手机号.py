import re

text = '重要的事情说8130123456789遍，\
我的手机号是13512346789这个靓号，\
不是15600998765，也不是110或119，\
王大锤的手机号才是1560099876512。'

pattern = re.compile(r'(?<!\d)(13\d{9}|14[57]\d{8}|15[012356789]\d{8}(?!\d)|17[678]\d{8}|18[0-9]\d{8})(?!\d)')
phone = re.findall(pattern, text)

for i in phone:
    print(i)
